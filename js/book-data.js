/**
 * Helper function to get chapter by ID
 */
function getChapter(id) {
    return BOOK_DATA.chapters.find(ch => ch.id === id);
}

/**
 * Helper function to get part by number
 */
function getPart(partNumber) {
    return BOOK_DATA.parts.find(p => p.partNumber === partNumber);
}

/**
 * Helper function to get chapters in a specific part
 */
function getChaptersInPart(partNumber) {
    return BOOK_DATA.chapters.filter(ch => ch.partNumber === partNumber);
}
